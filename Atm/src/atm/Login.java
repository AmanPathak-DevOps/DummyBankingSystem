    public Login() {
        t = new Timer(1000, (ActionEvent e) -> {
            m=i/60;
            s=i%60;
            i--;
            if(i>=0){
                Countdown.setText("Time Left :- "+m+" : "+s+" S");
            }if(i==0){
                JOptionPane.showMessageDialog(null,"Session Expired","Error",JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(null,"Please Reset The Values And Enter Again !");
                acnumber.setEditable(false);
                pwd.setEditable(false);
            }
        });t.start();
        initComponents();
        showDate();
        showTime();
    }
