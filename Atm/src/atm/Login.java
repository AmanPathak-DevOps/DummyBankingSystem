    void showDate() {
        Date d = new Date();
        SimpleDateFormat st=new SimpleDateFormat("dd:MM:yyyy");
        Date.setText(st.format(d));
    }
    void showTime(){
     
        ti = new Timer(0 , (ActionEvent e) -> {
            Date dt=new Date();
            SimpleDateFormat sdt= new SimpleDateFormat("E hh:mm:ss");
            String tt=sdt.format(dt);
            Time.setText(tt);
        });ti.start();
    }

